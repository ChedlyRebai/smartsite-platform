export const t = (
  lang: "en" | "fr" | "ar",
  key: string,
  defaultValue: string = key,
) => {
  const keys = key.split(".");
  let value: any = translations[lang];

  for (const k of keys) {
    value = value?.[k];
  }

  return value || defaultValue;
};

